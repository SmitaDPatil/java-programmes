package dataTypes;

public class BooleanUse {

	public static void main(String[] args) {
		// Single status is on-->true
		//Single status is off-->false
		// I am pass-->true, I am fail-->false
		//1. Variable declaration
		boolean result;
		//2. Variable initialization
		result=true; //true--> pass ,false--> fail
		// Usage
		System.out.println("My result status is "+result);
		
		// i want to store switch status--> ON ,OFF
		//ON-->true, OFF--> False
		boolean switchstatus;
		switchstatus=true;
		System.out.println("switch status is "+switchstatus);
		
		
		
		
		
	}

}
