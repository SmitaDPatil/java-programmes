package practiseDatatypes;

public class For {

	public static void main(String[] args) {
		// table of 5
		
		for( int a=5; a<=50; a=a+5)
			
		{
			System.out.println(a);
		}
		
		{
			System.out.println("=======================================");
		
		
		
	}

		for(int i=1; i<=10; i++)
		{
			System.out.println(i);
		}
		
		// kolhapur 10 time print
		
		String city="kolhapur";
		
		for(int j=1; j<=10; j++)
		{
			System.out.println(city);
		}
		
	}

}
