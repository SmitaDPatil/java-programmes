package practiseDatatypes;

public class Switch {

	public static void main(String[] args) {
		
		 int year=4;
		 
		 switch (year) {
		 case 1:
			 System.out.println("welcome to 1st year engg");
		break;
		 case 2:
			 System.out.println("welcome to 2 nd year engg");
		break;
		 case 3:
			 System.out.println("welcome to 3 rd year engg");
		break;
		 case 4:
			 System.out.println("welcome to 4 th year engg");
		 break;
		 
		 default:
			 System.out.println("please enter value between 1-4");
			 break;
		 }
			 
			
	}

}
